    MAINTENANCE_NO INT PRIMARY KEY,
    PARTY_NAME VARCHAR(100) NOT NULL,
    MAINTENANCE_DESCRIPTION VARCHAR(255),
    MAINTENANCE_DATE DATE NOT NULL,
    QUANTITY DECIMAL(10,2),
